namespace Mud.Client.Input;

/// <summary>
/// Keyboard key codes matching JavaScript KeyboardEvent.code values.
/// These represent physical key positions, independent of keyboard layout.
/// </summary>
public enum Code
{
    ArrowUp,
    ArrowDown,
    ArrowLeft,
    ArrowRight,
    KeyW,
    KeyA,
    KeyS,
    KeyD,
    Tab,
    KeyF,
    KeyC,
    Enter,
    Escape
}
